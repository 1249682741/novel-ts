  private tableHeight:number
  mounted(){
    this.$nextTick(() => {
      this.tableHeight = document.getElementById('table').offsetHeight
    })
  }

  showImage () {
      html2canvas(document.getElementById('table'), {
        backgroundColor: '#fff',
        width: parseInt((this.$refs.table as any).bodyWidth),
        windowWidth: parseInt((this.$refs.table as any).bodyWidth),
        x: 0,
        y: 0
      }).then(canvas => {
        const SPACING = 50
        let contentWidth = canvas.width
        let contentHeight = canvas.height
        let pageHeight = contentWidth / 592.28 * 841.89
        let leftHeight = contentHeight
        let position = 0
        let imgWidth = 595.28 - SPACING
        let imgHeight = (592.28) / contentWidth * contentHeight
        let pageData = canvas.toDataURL('image/jpeg', 1.0)
        let PDF = new jsPDF('p', 'pt', 'a4')
        if (leftHeight < pageHeight) {
          PDF.addImage(pageData, 'JPEG', SPACING / 2, 0, imgWidth, imgHeight)
        } else {
          while (leftHeight > 0) {
            PDF.addImage(pageData, 'JPEG', SPACING / 2, position, imgWidth, imgHeight)
            leftHeight -= pageHeight
            position -= 841.89
            if (leftHeight > 0) {
              PDF.addPage()
            }
          }
        }
        PDF.save('contetn.pdf')
      })
    }
}
